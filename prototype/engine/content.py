"""The domain library: mundane, eighth-grade-or-below, audience-guessable
subjects, each with a small question bank. image_prompt is a plain-text
description of what's being tested; it always doubles as the fallback shown
to the audience and as the dedup key in used_questions. Phase 2 (Revision 23)
adds image_url and image_credit alongside it: a real photo, sourced from
Wikimedia Commons pre-production (see fetch_images.py and image_library.json
next to this file), that the frontend shows instead of the plain-text
description whenever one is available. A question with no image_url yet
still works exactly as it did in Phase 1, falling back to the text prompt,
so the two phases can coexist domain by domain while the image library
fills in.

Each domain carries fifty questions (Revision 22, grown from twenty eight),
sized to comfortably outlast even a hot domain that gets inherited and
retested repeatedly across a whole show. A hot, repeatedly inherited domain
can still be drawn upward of fifty times in a worst-case show, so repeats
are not mathematically impossible, only rare, and only ever a last resort
once a domain's whole pool is exhausted within the show; see game.py's
shared used_questions tracking.

The library starts at fifty prepared domains per the design doc; this file
ships a real, usable subset so the engine can run full shows today. Growing
it to fifty domains (as opposed to fifty questions per domain, already done)
is just adding more entries in this same shape.
"""
from __future__ import annotations
from dataclasses import dataclass, replace
from typing import List, Optional
import json
import os
import random


@dataclass(frozen=True)
class Question:
    image_prompt: str
    answer: str
    image_url: Optional[str] = None
    image_credit: Optional[str] = None


@dataclass(frozen=True)
class Domain:
    name: str
    questions: List[Question]


def _d(name: str, pairs: List[tuple]) -> Domain:
    return Domain(name=name, questions=[Question(p, a) for p, a in pairs])


_IMAGE_LIBRARY_PATH = os.path.join(os.path.dirname(__file__), "image_library.json")


def _load_image_library() -> dict:
    """Pre-production image fetch results, keyed by 'DomainName|||prompt'.
    Optional: a missing, empty, or corrupt file just means no domain has
    had its images fetched yet (or a fetch run was interrupted mid-write),
    and every question falls back to its text prompt rather than crashing
    the whole engine over a content asset file."""
    if not os.path.exists(_IMAGE_LIBRARY_PATH):
        return {}
    try:
        with open(_IMAGE_LIBRARY_PATH, "r", encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return {}


def _apply_image_library(domains: List[Domain]) -> List[Domain]:
    library = _load_image_library()
    if not library:
        return domains
    patched = []
    for d in domains:
        new_questions = []
        for q in d.questions:
            key = f"{d.name}|||{q.image_prompt}"
            entry = library.get(key)
            if entry:
                q = replace(q, image_url=entry.get("url"), image_credit=entry.get("credit"))
            new_questions.append(q)
        patched.append(Domain(name=d.name, questions=new_questions))
    return patched


